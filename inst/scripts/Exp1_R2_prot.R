library("MSnbase")
library("DAPAR")

exprsFile <- system.file("extdata", "Exp1_R2_prot.txt", package="DAPARdata")

metadataFile <- system.file("extdata", "samples_Exp1_R2.txt", package="DAPARdata")
metadata = read.table(metadataFile, header=TRUE, sep="\t", as.is=TRUE)
indExpData <- c(49:54)
indFData <- c(1:48, 55:86)
versions <- list(Prostar_Version = "2.0",
                 DAPAR_Version = "2.0")
Exp1_R2_prot <- createMSnset(exprsFile, 
                          metadata, 
                          indExpData, 
                          indFData, 
                          keyId='Majority_protein_IDs', 
                          indexForOriginOfValue = c(36:41),
                          logData=FALSE, 
                          replaceZeros=TRUE,
                          typeOfData = "protein",
                          parentProtId = NULL,
                          versions)
fData(Exp1_R2_prot)[,"Intensity"] <- as.numeric(fData(Exp1_R2_prot)[,"Intensity"])
